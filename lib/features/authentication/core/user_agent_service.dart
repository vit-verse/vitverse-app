  String? _currentUserAgent;
  int _fallbackIndex = 0; // Circular rotation index for fallback

  /// Get authorised UA (cached or fetch once)
  /// One UA, cached, reused until blocked
  Future<String> getAuthorizedUserAgent() async {
    // 1. Use in-memory if available
    if (_currentUserAgent != null) {
      Logger.d('UserAgent', 'Using in-memory cached UA');
      return _currentUserAgent!;
    }

    // 2. Try SharedPreferences cache
    final prefs = await SharedPreferences.getInstance();
    _currentUserAgent = prefs.getString(_cacheKey);

    if (_currentUserAgent != null) {
      Logger.i('UserAgent', 'Loaded UA from SharedPreferences');
      Logger.d(
        'UserAgent',
        'Current UA: ${_currentUserAgent!.substring(0, _currentUserAgent!.length > 50 ? 50 : _currentUserAgent!.length)}...',
      );
      return _currentUserAgent!;
    }

    // 3. Fetch from server (first time or after block)
    return await _fetchFromServer();
  }

  /// Fetch fresh UA from Vercel server
  Future<String> _fetchFromServer() async {
    try {
      Logger.i('UserAgent', 'Fetching authorised UA from Vercel server...');

      final response = await http
          .get(Uri.parse(_serverUrl), headers: {'Accept': 'application/json'})
          .timeout(const Duration(seconds: 10));

      if (response.statusCode == 200) {
        final data = jsonDecode(response.body) as Map<String, dynamic>;
        _currentUserAgent = data['authorisedUserAgent'] as String;

        final indexReturned = data['indexReturned'] ?? 0;
        final total = data['total'] ?? 0;
        final updated = data['updated'] ?? 'unknown';

        // Cache it
        final prefs = await SharedPreferences.getInstance();
        await prefs.setString(_cacheKey, _currentUserAgent!);

        Logger.success('UserAgent', 'Fetched and cached new UA from server');
        Logger.d(
          'UserAgent',
          'UA index=$indexReturned / total=$total (updated: $updated)',
        );
        Logger.d(
          'UserAgent',
          'Current UA: ${_currentUserAgent!.substring(0, _currentUserAgent!.length > 50 ? 50 : _currentUserAgent!.length)}...',
        );

        return _currentUserAgent!;
      } else {
        Logger.w(
          'UserAgent',
          'Server returned ${response.statusCode}, using fallback',
        );
      }
    } catch (e) {
      Logger.w('UserAgent', 'Failed to fetch from Vercel server: $e');
    }

    // Use fallback UA pool with circular rotation
    _currentUserAgent = _fallbackUAs[_fallbackIndex];
    _fallbackIndex = (_fallbackIndex + 1) % _fallbackUAs.length;

    Logger.w(
      'UserAgent',
      'Using fallback UA #${_fallbackIndex + 1}/${_fallbackUAs.length}',
    );

    // Cache fallback too
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString(_cacheKey, _currentUserAgent!);

    return _currentUserAgent!;
  }

  /// Called ONLY when VTOP returns "not authorized"
  /// -> rotate only when blocked!
  Future<String> handleBlockedUserAgent() async {
    Logger.w('UserAgent', 'ðŸš« User agent blocked, fetching new one...');

    // Clear cache
    _currentUserAgent = null;
    final prefs = await SharedPreferences.getInstance();
    await prefs.remove(_cacheKey);

    // Fetch fresh from Vercel (gets next UA in sequence)
    final newUA = await _fetchFromServer();

    Logger.i(
      'UserAgent',
      'Rotated to new UA: ${newUA.substring(0, newUA.length > 50 ? 50 : newUA.length)}...',
    );

    return newUA;
  }

  /// Get current user agent without fetching
  String? getCurrentUserAgent() => _currentUserAgent;

  /// Force refresh
  Future<String> refreshUserAgent() async {
    Logger.i('UserAgent', 'Force refreshing user agent from Vercel...');
    return await handleBlockedUserAgent();
  }
}
