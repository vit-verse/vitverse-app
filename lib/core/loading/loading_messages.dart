  static final Map<String, List<String>> _usedMessages = {};

  /// Get a random message for a specific feature
  /// Ensures no repetition until all messages are used
  static String getMessage(String featureKey) {
    final messages = _messages[featureKey] ?? _messages['default']!;
    final usedForFeature = _usedMessages[featureKey] ?? <String>[];

    // If all messages used, reset the list
    if (usedForFeature.length >= messages.length) {
      _usedMessages[featureKey] = <String>[];
    }

    // Get unused messages
    final availableMessages =
        messages.where((msg) => !usedForFeature.contains(msg)).toList();

    if (availableMessages.isEmpty) {
      return messages.first;
    }

    // Pick random unused message
    final randomMessage =
        availableMessages[DateTime.now().millisecondsSinceEpoch %
            availableMessages.length];

    // Mark as used
    _usedMessages[featureKey] = [...usedForFeature, randomMessage];

    return randomMessage;
  }

